// Déclaration de la fonction
function greetings(firstname, lastname) {
    return "Bienvenue " + firstname + " " + lastname;
}

// Appel de la fonction pour exécution
greetings("Harold", "Pierrache");
greetings("Hector", "Pierrache");
var speach = greetings("Suzane", "Pierrache");
